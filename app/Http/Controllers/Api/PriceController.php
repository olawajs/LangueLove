     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'type_id' => 'required|integer',
            'price_type_id' => 'required|integer',
            'duration_id' => 'required|integer',
            'price' => 'required|numeric',
            'certification' => 'required|boolean'
        ]);

        $type = Price::updateOrCreate(
            ['type_id' => $request->type_id, 'price_type_id' => $request->price_type_id, 'duration_id' => $request->duration_id, 'certification' => $request->certification],
            ['price' => $request->price]
        );

        if($type){
            return 1;
        }
        else{
            return 0;
        }
    }

