        $validated = $request->validate([
            'name' => 'required|string|max:255',
            'short' => 'required|string|max:10',
            'price_type' => 'required|integer',
            'active' => 'required|boolean'
        ]);
    
        $language = Language::create($validated);
        if($language){
            return 1;
        }
        else{
            return 0;
        }
    }

